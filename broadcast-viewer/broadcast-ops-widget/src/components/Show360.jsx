function FlagsCard({ flags }) {
  if (!flags.length) return null;
  return (
    <ModalCard title="Content Advisories" eyebrow="Auto-synced from data lake" icon={AlertTriangle} iconColor="#d97706">
      <div style={{ display: "grid", gridTemplateColumns: "repeat(4, minmax(0, 1fr))", gap: "12px" }}>
        {flags.map((f) => {
          const Icon = FLAG_ICONS[f.kind] || AlertCircle;
          return (
            <div key={f.kind} style={{ borderRadius: "4px", padding: "12px", background: "#fef3c7", border: "1px solid #fde68a" }}>
              <Icon style={{ width: "16px", height: "16px", marginBottom: "8px", color: "#92400e" }} />
              <div style={{ fontSize: "12px", fontWeight: 700, color: "#92400e" }}>{f.kind}</div>
              <div style={{ fontSize: "11px", textTransform: "uppercase", letterSpacing: "0.05em", marginTop: "2px", color: "#b45309" }}>{f.level}</div>
            </div>
          );
        })}
      </div>
      <p style={{ fontSize: "12px", marginTop: "12px", fontStyle: "italic", color: "#6b6a63", margin: "12px 0 0 0" }}>Advisories automatically generated by upstream scheduling + screening systems.</p>
    </ModalCard>
  );
}

